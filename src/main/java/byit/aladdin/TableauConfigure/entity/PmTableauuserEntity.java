package byit.aladdin.TableauConfigure.entity;

import java.io.Serializable;
/**
 * table权限配置
 * @author tanghuabo  
 */

@SuppressWarnings("serial")

public class PmTableauuserEntity implements Serializable{
	// ==============================Fields========================================
	private String userid;
	private String optype;// 操作类型
	private String username;//tableau用户名
	private String password;//tableau密码
	private String projectname;//项目名称
	private String tableauip;//tableau服务器ip
	// ==============================RelevanceFields==================================
	// ==============================PropertyAccessors================================
	public PmTableauuserEntity() {
		super();
		// TODO Auto-generated constructor stub
	}
	public PmTableauuserEntity(String userid, String optype, String username, String password, String projectname,
			String tableauip) {
		super();
		this.userid = userid;
		this.optype = optype;
		this.username = username;
		this.password = password;
		this.projectname = projectname;
		this.tableauip = tableauip;
	}
	public String getUserid() {
		return userid;
	}
	public void setUserid(String userid) {
		this.userid = userid;
	}
	public String getOptype() {
		return optype;
	}
	public void setOptype(String optype) {
		this.optype = optype;
	}
	public String getUsername() {
		return username;
	}
	public void setUsername(String username) {
		this.username = username;
	}
	public String getPassword() {
		return password;
	}
	public void setPassword(String password) {
		this.password = password;
	}
	public String getProjectname() {
		return projectname;
	}
	public void setProjectname(String projectname) {
		this.projectname = projectname;
	}
	public String getTableauip() {
		return tableauip;
	}
	public void setTableauip(String tableauip) {
		this.tableauip = tableauip;
	}
}
