package byit.aladdin.TableauConfigure.entity;

import java.io.Serializable;

/**
 * table数据库配置
 * @author tanghuabo
 */
@SuppressWarnings("serial")
public class PmTableauserverEntity implements Serializable{
	// ==============================Fields========================================
	private String serverid;// 主键

	private String tableauserverip;// postgre服务器IP

	private String postgreurl;// postgreURL

	private String postgreuname;// postgre用户名

	private String postgrepwd;// postgre密码
	// ==============================RelevanceFields==================================
	// ==============================PropertyAccessors================================
	public PmTableauserverEntity() {
		super();
		// TODO Auto-generated constructor stub
	}
	public PmTableauserverEntity(String serverid, String tableauserverip, String postgreurl, String postgreuname,
			String postgrepwd) {
		super();
		this.serverid = serverid;
		this.tableauserverip = tableauserverip;
		this.postgreurl = postgreurl;
		this.postgreuname = postgreuname;
		this.postgrepwd = postgrepwd;
	}
	public String getServerid() {
		return serverid;
	}
	public void setServerid(String serverid) {
		this.serverid = serverid;
	}
	public String getTableauserverip() {
		return tableauserverip;
	}
	public void setTableauserverip(String tableauserverip) {
		this.tableauserverip = tableauserverip;
	}
	public String getPostgreurl() {
		return postgreurl;
	}
	public void setPostgreurl(String postgreurl) {
		this.postgreurl = postgreurl;
	}
	public String getPostgreuname() {
		return postgreuname;
	}
	public void setPostgreuname(String postgreuname) {
		this.postgreuname = postgreuname;
	}
	public String getPostgrepwd() {
		return postgrepwd;
	}
	public void setPostgrepwd(String postgrepwd) {
		this.postgrepwd = postgrepwd;
	}
}

