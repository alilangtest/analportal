package byit.aladdin.TableauConfigure.bo;

/**
 *  系统参数配置
 */
public class SystemDomainBo {
	
	private String eventid;//主键
	private String domainid;//值域类型
	private String domainname;//值域类型名称
	private String codeid;//key值
	private String codename;//value值
	public String getEventid() {
		return eventid;
	}
	public void setEventid(String eventid) {
		this.eventid = eventid;
	}
	public String getDomainid() {
		return domainid;
	}
	public void setDomainid(String domainid) {
		this.domainid = domainid;
	}
	public String getDomainname() {
		return domainname;
	}
	public void setDomainname(String domainname) {
		this.domainname = domainname;
	}
	public String getCodeid() {
		return codeid;
	}
	public void setCodeid(String codeid) {
		this.codeid = codeid;
	}
	public String getCodename() {
		return codename;
	}
	public void setCodename(String codename) {
		this.codename = codename;
	}
}
