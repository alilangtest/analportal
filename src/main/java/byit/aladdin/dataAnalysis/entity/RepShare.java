package byit.aladdin.dataAnalysis.entity;

import java.io.Serializable;
import java.util.Date;

public class RepShare implements Serializable{

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;
	
	
	private String userId;
	private String reportId;
	private String startTime;
	private String sharId;
	private Date valldTime;
	private String createTime;
	private String creatorId;
	
	public String getUserId() {
		return userId;
	}
	public void setUserId(String userId) {
		this.userId = userId;
	}
	public String getReportId() {
		return reportId;
	}
	public void setReportId(String reportId) {
		this.reportId = reportId;
	}
	public String getStartTime() {
		return startTime;
	}
	public void setStartTime(String startTime) {
		this.startTime = startTime;
	}
	public String getSharId() {
		return sharId;
	}
	public void setSharId(String sharId) {
		this.sharId = sharId;
	}
	
	public Date getValldTime() {
		return valldTime;
	}
	public void setValldTime(Date valldTime) {
		this.valldTime = valldTime;
	}
	public String getCreateTime() {
		return createTime;
	}
	public void setCreateTime(String createTime) {
		this.createTime = createTime;
	}
	public String getCreatorId() {
		return creatorId;
	}
	public void setCreatorId(String creatorId) {
		this.creatorId = creatorId;
	}

}
