package byit.tableausubscribe.tab.bean;


/**
 * 邮件返回结构实例
 * <result>
		<reportId>仪表板[,仪表板1...]</reportId>
		<mail id="邮箱1">
			<sendtime>实际发送时间</sendtime>
			<sendState>发送状态:Y,N</sendState>
		</mail>
		<mail id="邮箱2">
			<sendtime>实际发送时间</sendtime>
			<sendState>发送状态:Y,N</sendState>
		</mail>
	</result>
 */
public class SendResult{
	private String reportId;//报表
	private String emailId;//邮件
	private String sendTime;//发送时间
	private String sendState;//发送状态
	
	public String getReportId() {
		return reportId;
	}
	public void setReportId(String reportId) {
		this.reportId = reportId;
	}
	public String getEmailId() {
		return emailId;
	}
	public void setEmailId(String emailId) {
		this.emailId = emailId;
	}
	public String getSendTime() {
		return sendTime;
	}
	public void setSendTime(String sendTime) {
		this.sendTime = sendTime;
	}
	public String getSendState() {
		return sendState;
	}
	public void setSendState(String sendState) {
		this.sendState = sendState;
	}
	@Override
	public String toString() {
		return "SendResult [reportId=" + reportId + ", emailId=" + emailId + ", sendTime=" + sendTime + ", sendState=" + sendState + "]";
	}

	
}
