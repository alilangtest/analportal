package byit.osdp.base.security.exception;

import org.apache.shiro.authc.AuthenticationException;

@SuppressWarnings("serial")
public class UserNoRoleAuthcException extends AuthenticationException{
	public UserNoRoleAuthcException() {
	}

	public UserNoRoleAuthcException(String message) {
		super(message);
	}

	public UserNoRoleAuthcException(Throwable cause) {
		super(cause);
	}

	public UserNoRoleAuthcException(String message, Throwable cause) {
		super(message, cause);
	}
}
